const RESOLUTIONS: Array<"HD" | "4K"> = ["HD", "4K"];
const DEVICE_TYPES: Array<"desktop" | "mobile"> = ["desktop", "mobile"];

const VARIANTS = [
  "Vol.2",
  "Remastered",
  "4K Edition",
  "Extended",
  "Director's Cut",
  "Alternate",
  "2160p",
  "Fan Art",
  "Concept Art",
  "Dark Version",
  "Light Version",
  "Anniversary",
];

function buildFromDefs(defs: MovieDef[], startId: number): Wallpaper[] {
  const wallpapers: Wallpaper[] = [];
  let id = startId;

  for (const movie of defs) {
    const count = Math.ceil(1000 / defs.length);

    for (let i = 0; i < count; i++) {
      const titleBase = movie.titles[i % movie.titles.length];
      const variantNum = Math.floor(i / movie.titles.length);
      const title =
        variantNum === 0
          ? titleBase
          : `${titleBase} ${VARIANTS[variantNum % VARIANTS.length]}`;

      const imgKey = movie.imgs[i % movie.imgs.length];
      const resolution = RESOLUTIONS[i % 2];
      const deviceType = DEVICE_TYPES[Math.floor(i / 2) % 2];
      const isTrending = i < 3;

      const wRes = resolution === "4K" ? "3840" : "1920";

      wallpapers.push({
        id,
        title,
        category: "Movies",
        resolution,
        deviceType,
        imageUrl: `https://images.unsplash.com/${imgKey}?w=800&q=80`,
        fullUrl: `https://images.unsplash.com/${imgKey}?w=${wRes}&q=95`,
        tags: [
          ...movie.tags,
          resolution.toLowerCase(),
          "movie",
          "movies",
          "film",
          "4k",
          "hd",
        ],
        isTrending,
      });

      id++;
    }
  }

  return wallpapers;
}

